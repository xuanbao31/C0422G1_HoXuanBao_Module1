package ss1_introduction.thuc_hanh;

public class Main {
    public static void main(String[] args) {
        int i = 10; //Biến i kiểu số nguyên, có giá trị là 10
        float f = 20.5f;//Biến f kiểu số thực, có giá trị là 20.5
        double d=20.5;//Biến d kiểu số thực lớn, có giá trị là 20.5
        boolean b =true;//Biến b kiểu logic, có giá trị là true
        char c = 'a';//Biến c kiểu ký tự, có giá trị là 'a'
        String s = "Ha noi";//Biến s kiểu chuỗi, có giá trị là "Hà Nội"
        System.out.println("i ="+ i);
        System.out.println("f ="+f);
        System.out.println("d ="+d);
        System.out.println("b ="+b);
        System.out.println("c ="+c);
        System.out.println("s ="+s);
    }
}

